--PROCEDURE TO FIND THE SQUAREROOT OF AN INTEGER NUMBER

CREATE OR REPLACE PROCEDURE EMP.PROCS.SP_FIND_SQRT("IP_NUMBER" INTEGER)
RETURNS FLOAT
LANGUAGE SQL
COMMENT = 'THIS IS SAMPLE PROGRAM TO FIND SQRT OF INTEGER'
EXECUTE AS CALLER
AS

DECLARE
    SQROOT FLOAT;
    
BEGIN
    SQROOT := SQRT(IP_NUMBER);
    RETURN SQROOT;
END;

